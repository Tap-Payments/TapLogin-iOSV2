/// Helper enum to differentiate localization keys required for TapCountriesKit.
///
/// - navigataionBarCancelTitle: Key for cancel button title on the navigation bar of CountriesListViewController
/// - navigationBarDoneTitle: Key for done button title on the navigation bar of CountriesListViewController
/// - navigationBarTitle: Key for title of the navigation bar of CountriesListViewController
/// - searchBarPlaceholder: Key for search bar placeholder of CountriesListViewController
/// - searchBarCancelTitle: Key for search bar cancel button of CountriesListViewController
public enum CountriesKitLocalization {

    case navigataionBarCancelTitle
    case navigationBarDoneTitle
    case navigationBarTitle
    case searchBarPlaceholder
    case searchBarCancelTitle
}
