/// Helper enum to differentiate localization keys required for TapLocalization-UI.
///
/// - navigataionBarCancelTitle: Key for cancel button title on the navigation bar of LanguageSelectionViewController
/// - navigationBarDoneTitle: Key for done button title on the navigation bar of LanguageSelectionViewController
/// - navigationBarTitle: Key for title of the navigation bar of LanguageSelectionViewController
public enum LocalizationLocalization {

    case navigationBarCancelTitle
    case navigationBarDoneTitle
    case navigationBarTitle
}
